public class FDLDetail {
	public static void main(String[] args) {
		int counter = 10;
		
		// float salary = 12000.00f;
		float salary = 12000.00F;
		
		// double interestRate  = 0.0075;
		double interestRate  = 0.0075D;

		long countryPopulation = 12010191919L;
		
		System.out.println(" counter value: " + counter);
		System.out.println(" salary value: " + salary);
		System.out.println(" interestRate value: " + interestRate);
		System.out.println(" countryPopulation value: " + countryPopulation);
		
	}

}