
public class SwitchCaseDemo {
	public static void main(String[] args) {
		int dayNo = 1;
		switch (dayNo) {
			case 1:
				System.out.println("Day is Monday");
				break;
			case 2:
				System.out.println("Day is Tuesday");
				break;
			default:
				System.out.println("Invalid Day");
				break;
			}
		System.out.println("End of program");
	}

}